# 🔢 Koans zu Zahlen (Integer)
# Ersetze __ durch den richtigen Wert, sodass kein Fehler mehr auftritt.

__ = None  # Platzhalter – bitte ersetzen!

# 🧮 Was ist der Typ von 5?
z = 5
assert type(z) == __, "Welcher Typ ist das?"

# 🔤 Umwandlung von String zu Zahl
fuenf = "5"
assert type(fuenf) == __, "Welcher Typ ist das?"
noch_ein_fuenf = int("5")
assert type(noch_ein_fuenf) == __, "Welcher Typ ist das?"

# ➕➖✖️➗ Rechnen mit Zahlen
assert 1 + 3 == __, "Was ergibt 1 + 3?"
assert 3 * 3 == __, "Was ergibt 3 * 3?"
assert 3 ** 2 == __, "Was ergibt 3 hoch 2?"
assert 9 / 3 == __, "Was ergibt 9 geteilt durch 3?"
assert 6 - 3 == __, "Was ergibt 6 minus 3?"

# 🔍 Vergleiche
assert (1 < 3) == __, "Ist 1 kleiner als 3?"
assert (3 > 3) == __, "Ist 3 größer als 3?"
assert (3 == 2) == __, "Sind 3 und 2 gleich?"
assert (9 <= 3) == __, "Ist 9 kleiner oder gleich 3?"
assert (6 >= 3) == __, "Ist 6 größer oder gleich 3?"
